export function sum(x, y) {
  return x + y;
}

export function rest(x, y) {
  return x - y;
}

export function mult(x, y) {
  return x * y;
}

export function div(x, y) {
  return x / y;
}

export default sum // el default es lo que por defecto se exportara si ninguna otra cosa a sido exportada en un documento